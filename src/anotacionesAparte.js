//  EJEMPLO DE FUNCIÓN CORTA DE FLECHA :
//  const message = () => 'Hello World'
//  No necesita un return y lo que devolverá será el valor empleado como cuerpo de la función (hello world). Esto sucede con las arrow functions que se pueden poner en una sola línea.
//  Si queremos devolver un objeto hay que envolver el cuerpo de la función con parénteisis.
//  const person = () => ({ name: 'Julen', age: 24 })
//  Si sólo tienen un argumento no hace falta especificar los paréntesis, pero si tiene más argumentos sí:
//  const triple = x => x+3    //   triple(3) devuelve 9
//  Valores por defecto:
//  const pow = (a, b = 2) => Math.pow(a, b)    Lo que hace es que si sólo pones un argumento, el segundo siempre será 2, pero si pones dos argumentos se sobreescribe el segundo por el argumento que introduzcas al llamar a la función

